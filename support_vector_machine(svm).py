#1. Gaussian Kernel 그리기
import numpy as np
import matplotlib.pyplot as plt

def GaussianKernel(v1,v2,sigma):
  res = np.exp(-np.linalg.norm(v1-v2)**2//2*sigma**2)
  return res

#1.1 x1, x2, z 값 구하기
d = 100 #split
x1 = np.linspace(0,1,d) #x1축
x2 = np.linspace(0,10,d) #x2 축
l1 = [3,5]
z = np.zeros((d,d))
sigma = 1.5
for i,t1 in enumerate(x1):
  for j, t2 in enumerate(x2):
    val = GaussianKernel(np.array([t1,t2]),l1,sigma)
    z[i,j] = val

#2. 그리기
from mpl_toolkits import mplot3d
px,py = np.meshgrid(x1,x2)
fig = plt.figure()
ax = plt.axes(projection = '3d')
ax.contour3D(px,py,z,100,cmap='jet')
ax.set_xlabel('x1')
ax.set_ylabel('x2')
ax.set_zlabel('Z');
plt.show()

