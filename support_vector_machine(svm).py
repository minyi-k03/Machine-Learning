import numpy as np
from scipy.io import loadmat
data = loadmat('data1.mat')
x = data['X'] #xlabel 데이터
y = data['y'] #ylabel 데이터
m = len(y)

#2.2 선형 SVM 분류 :: 그래프 그리기
import matplotlib.pyplot as plt
pos = []
neg = []

#참 거짓 분류
for i,val in enumerate(y):
  if val == 1:
    pos.append(i)
  else:
    neg.append(i)

plt.plot(x[pos,0].reshape(-1), x[pos,1].reshape(-1), 'b+', label = 'y=1')
plt.plot(x[neg,0].reshape(-1),x[neg,1].reshape(-1),'ro', label = 'y=0')
plt.xlabel('x1')
plt.ylabel('x2')
plt.legend(loc = 'upper right')
plt.title("Data 1")
plt.show()

#3. 학습
from sklearn.pipeline import Pipeline
from sklearn.preprocessing import StandardScaler
from sklearn.svm import LinearSVC

svm_clf = Pipeline([
    ("scaler",StandardScaler()),
    ("linear_svc",LinearSVC(C=1, loss = "hinge"))
])

svm_clf.fit(x,y) #모델 학습

svm_clf.predict([[2.3,2.7],[3.7,3.9]]) #x1, x2, x1**2, x2**2

#4. Decision Boundary

x_min, x_max = x[:,0].min()-0.2, x[:,0].max()+0.2
y_min, y_max = x[:,1].min()-0.2, x[:,1].max()+0.2
h = .02
xx, yy = np.meshgrid(np.arange(x_min, x_max,h), np.arange(y_min,y_max,h))
z = svm_clf.predict(np.c_[xx.ravel(),yy.ravel()])

#색상 넣는 코드
z = z.reshape(xx.shape)
plt.figure(1)
plt.pcolormesh(xx,yy,z,cmap = plt.cm.Paired)

plt.plot(x[pos,0].reshape(-1),x[pos,1].reshape(-1),'b+',label = 'y=1')
plt.plot(x[neg,0].reshape(-1),x[neg,1].reshape(-1),'ro',label='y=0')
plt.xlabel('x1')
plt.ylabel('x2')
plt.legend(loc = 'upper right')

plt.xlim(xx.min(),xx.max())
plt.ylim(yy.min(),yy.max())
plt.xticks(())
plt.yticks(())
plt.show()

